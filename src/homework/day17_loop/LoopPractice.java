package homework.day17_loop;

public class LoopPractice {
    public static void main(String[] args) {
        /*
        write a program that will add all the numbers from 1 - 500
         */
        int count = 1;
        int sum = 500;

        do {
            System.out.print(count + " ");
            count++;
        } while(count<=sum);



        }
    }
