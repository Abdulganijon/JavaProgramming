public class ArrayWithAmin {
    public static void main(String[] args) {
        String[] cars = {"bmw","merc","lexus","inf", "lambo"};
        System.out.println(cars[0]);
        for (int i = 0; i < cars.length; i++) {
            System.out.println(cars[i]);

        }
    }

}
